package m09_my_first_program;

/*
Create Java class named PrintExecution that prints the following outputs in the console:
     Cydeo

     Yuliia
     Alena
     Lucy
     Alexander

     Igor
     Sumeye
     Madina
 */
public class PrintExecution {

    public static void main(String[] args) {

        System.out.println("Cydeo");
        System.out.println();
        System.out.println("Yuliia");
        System.out.println("Alena");
        System.out.println("Lucy");
        System.out.println("Alexander\n");
        System.out.println("Igor");
        System.out.println("Sumeye");
        System.out.println("Madina\n");

        System.out.println("----------------");

        System.out.print("Cydeo\n\nYuliaa\nAlena\nLucy\nAlexander\n\nIgor\nSumeye\nMadina");

    }
}
