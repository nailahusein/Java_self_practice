/*
Create a Java class named Diamond2 that prints the following outputs in the console;
 */

public class Diamond2 {

    public static void main(String[] args) {

        System.out.println("        ^         ");
        System.out.println("      /   \\       ");
        System.out.println("     /  *  \\      ");
        System.out.println("    /  *  * \\     ");
        System.out.println("   / *  *  * \\    ");
        System.out.println("   -----------     ");
        System.out.println("   \\ *  *  * /    ");
        System.out.println("    \\  *  * /     ");
        System.out.println("     \\  *  /      ");
        System.out.println("      \\   /       ");
        System.out.println("        v         ");
    }
}
